function hsv2rgb(h, s, v) {  // h: [0, 360], s: [0, 1], v: [0, 1]
    function ghr01(rm1, rm2, h) {
        h = (h > 360) ? (h - 360) : ((h < 0) ? (h + 360) : h);
        if (h < 60) {
	    return rm1 + (rm2 - rm1) * h / 60.0;
        }
        else if (h < 180) {
	    return rm2;
        }
        else if (h < 240) {
	    return rm1 + (rm2 - rm1) * (240 - h) / 60.0;
        }
        else {
	    return rm1;
        }
    }

    let r, g, b;
    let rm1, rm2;
    if (v < 0.5) {
	rm2 = v * (1.0 + s);
    }
    else {
	rm2 = v + s - v * s;
    }
    rm1 = 2.0 * v - rm2;

    if (s == 0) {
	r = g = b = v;
    }
    else {
	r = ghr01(rm1, rm2, h + 120);
	g = ghr01(rm1, rm2, h);
	b = ghr01(rm1, rm2, h - 120);
    }

    return [ r, g, b ];
}


export let ColorMap = function(name) {
    this.palette = null;
    this.underflowColor = null;
    this.overflowColor = null;
    this.isOutOfRangeIndicated = true;

    if (name == "Parula") {
        this.palette = ColorMapPalette_Parula;
        this.underflowColor = '#505050';
        this.overflowColor = '#f0f0d0';
    }
    else if (name == "Viridis") {
        this.palette = ColorMapPalette_Viridis;
        this.underflowColor = '#000000';
        this.overflowColor = '#f0f0d0';
    }
    else if (name == "Magma") {
        this.palette = ColorMapPalette_Magma;
        this.underflowColor = '#000000';
        this.overflowColor = '#f0f0d0';
    }
    else if (name == "DarkBodyRadiator") {
        this.palette = ColorMapPalette_DarkBodyRadiator;
        this.underflowColor = '#000000';
        this.overflowColor = '#f0f0d0';
    }
    else if (name == "UW") {
        this.palette = ColorMapPalette_UW;
        this.underflowColor = '#000000';
        this.overflowColor = '#f0f0d0';
    }
    else if (name == "UWGold") {
        this.palette = ColorMapPalette_UWGold;
        this.underflowColor = '#000000';
        this.overflowColor = '#f0f0d0';
    }
    else if (name == "MIT") {
        this.palette = ColorMapPalette_DarkBodyRadiator;
        this.underflowColor = '#000000';
        this.overflowColor = '#f0f0d0';
    }
    else if (name == "KIT") {
        this.palette = ColorMapPalette_KIT;
        this.underflowColor = '#000000';
        this.overflowColor = '#f0f0d0';
    }
    else if (name == "Gray") {
        this.palette = [];
        for (let i = 0; i < 256; i++) {
            let x = 0.8 * (i / 256.0) + 0.1;
            this.palette.push([x, x, x]);
        }
        this.underflowColor = '#000000';
        this.overflowColor = '#ffffff';
    }
    else {
        // Rainbow //
        this.palette = [];
        for (let i = 0; i < 256; i++) {
            let x = i / 256.0;
            let hue = parseInt(280*(1-x));
            this.palette.push(hsv2rgb(hue, 0.7, 0.5));
        }
        this.underflowColor = '#d0d0d0';
        this.overflowColor = '#800000';
    }

    this.indicateOutOfRange = function(isIndicated) {
        this.isOutOfRangeIndicated = isIndicated;
    }

    this.colorNameOf = function(x) {
        if (x < 0) {
            if (this.isOutOfRangeIndicated && this.underflowColor) {
                return this.underflowColor;
            }
            else {
                x = 0;
            }
        }
        if (x > 1) {
            if (this.isOutOfRangeIndicated && this.overflowColor) {
                return this.overflowColor;
            }
            else {
                x = 1;
            }
        }
        
        let n = this.palette.length;
        let i = parseInt(x * n);
        if (i < 0) {
            i = 0;
        }
        else if (i >= n) {
            i = n-1;
        }
        let rgb = this.palette[i];
        
        let r = parseInt(255 * rgb[0]);
        let g = parseInt(255 * rgb[1]);
        let b = parseInt(255 * rgb[2]);

        return 'rgb(' + r + ',' + g + ',' + b + ')';
    }
}
